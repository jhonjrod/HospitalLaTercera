    @Override
    public void actionPerformed(ActionEvent e) {
        if(e.getActionCommand().equals("Aceptar")){
            ControladorModificacion instancia1 = new ControladorModificacion();
            instancia1.setVentana6(this);
            instancia1.modificarUnacita();
            
        }
        if(e.getActionCommand().equals("ID de la Cita:")){
            if(jCheckBox1.isSelected()){
                jTextField1.setEnabled(true);
            }
            else{
                jTextField1.setEnabled(false);
            }
        }
        
        if(e.getActionCommand().equals("Fecha:")){
            if(jCheckBox2.isSelected()){
                jTextField2.setEnabled(true);
            }
            else{
                jTextField2.setEnabled(false);
            }
              
        }
        
        if(e.getActionCommand().equals("Hora:")){
            if(jCheckBox3.isSelected()){
                jTextField3.setEnabled(true);
            }
            else{
                jTextField3.setEnabled(false);
            }
        }
        
        if(e.getActionCommand().equals("ID del medico:")){
            if(jCheckBox4.isSelected()){
                jComboBox1.setEnabled(true);
            }
            else{
                jComboBox1.setEnabled(false);
            }
            
        }
        
        if(e.getActionCommand().equals("ID del paciente:")){
            if(jCheckBox5.isSelected()){
                jComboBox2.setEnabled(true);
            }
            else{
                jComboBox2.setEnabled(false);
            }
        }
    }
    
