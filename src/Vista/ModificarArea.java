    @Override
    public void actionPerformed(ActionEvent e) {
        if(e.getActionCommand().equals("Aceptar")){
            System.out.println("apunto de entrar a modificar area");
            ControladorModificacion instancia1 = new ControladorModificacion();
            instancia1.setVentana1(this);
            instancia1.modificarUnarea();
            
        }
        if(e.getActionCommand().equals("Codigo del Area:")){
            if(jCheckBox1.isSelected()){
                jTextField1.setEnabled(true);
            }
            else{
                jTextField1.setEnabled(false);
            }
        }
        
        if(e.getActionCommand().equals("Nombre:")){
            if(jCheckBox2.isSelected()){
                jTextField2.setEnabled(true);
            }
            else{
                jTextField2.setEnabled(false);
            }
            
        }
        
        if(e.getActionCommand().equals("Descripción:")){
            if(jCheckBox3.isSelected()){
                jTextField3.setEnabled(true);
            }
            else{
                jTextField3.setEnabled(false);
            }
        }
        
        if(e.getActionCommand().equals("Camas disponibles:")){
            if(jCheckBox4.isSelected()){
                jTextField4.setEnabled(true);
            }
            else{
                jTextField4.setEnabled(false);
            }
        }
    
    }
    
